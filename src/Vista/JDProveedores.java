    private boolean validar() {
        if (!band) {
            try {
                idproveedor = Integer.parseInt(txtProveedor.getText().trim());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "La id del proveedor debe ser un número entero.");
                txtProveedor.requestFocusInWindow();
                return false;
            }
        }

        nombrePila = txtNombre.getText().trim();
        apellidoPaterno = txtApellido.getText().trim();
        telefono = txtTelefono.getText().trim();
        email = txtEmail.getText().trim();

        if (nombrePila.equals("")) {
            JOptionPane.showMessageDialog(null, "Especifica el nombre del proveedor.");
            txtNombre.requestFocusInWindow();
            return false;
        }

        if (apellidoPaterno.equals("")) {
            JOptionPane.showMessageDialog(null, "Especifica el apellido del proveedor.");
            txtApellido.requestFocusInWindow();
            return false;
        }

        if (telefono.equals("")) {
            JOptionPane.showMessageDialog(null, "Especifica el teléfono del proveedor.");
            txtTelefono.requestFocusInWindow();
            return false;
        }

        if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Especifica el email del proveedor.");
            txtEmail.requestFocusInWindow();
            return false;
        }

        return true;
    }//fin del metodo validar

    private void imprimirMensajeDeErrorDAO(DAOException ex) {
        String mensajeError;

        try {
            mensajeError = ex.getCause().getMessage();
        } catch (NullPointerException error) {
            mensajeError = "";
        }

        JOptionPane.showMessageDialog(null,
                ex.getMessage() + "\n"
                + "Mensaje: " + mensajeError,
                "Error", JOptionPane.ERROR_MESSAGE);
    }// Fin del método imprimirMensajeDeError

