    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (validar()) {
            String nombre = txtNombre.getText().trim();
            String apellido = txtApellido.getText().trim();
            String telefono = txtTelefono.getText().trim();
            String email = txtEmail.getText().trim();

            Proveedor miproveedor = new Proveedor(nombre, apellido, telefono, email);

            try {
                if (band) {
                    // INSERTAR — no necesitas idProveedor
                    manager.getProveedorDAO().insertar(miproveedor);
                    JOptionPane.showMessageDialog(null, "El proveedor ha sido guardado");
                } else {
                    // MODIFICAR — sí necesitas idProveedor
                    int idproveedor = Integer.parseInt(txtProveedor.getText().trim());
                    miproveedor.setIdProveedor(idproveedor);
                    manager.getProveedorDAO().modificar(miproveedor);
                    JOptionPane.showMessageDialog(null, "El proveedor ha sido modificado");
                }
            } catch (DAOException ex) {
                imprimirMensajeDeErrorDAO(ex);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String idProveedorEliminar = txtProveedor.getText().trim();

        if (idProveedorEliminar.equals("")) {
            JOptionPane.showMessageDialog(null, "Busca primero un proveedor para eliminar");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(null,
                "¿Deseas eliminar al proveedor con ID = " + idProveedorEliminar + "?",
                "Confirmar", JOptionPane.YES_NO_OPTION);

        if (respuesta == JOptionPane.YES_OPTION) {
            try {
                manager.getProveedorDAO().eliminar(Integer.valueOf(idProveedorEliminar));
                JOptionPane.showMessageDialog(null, "El proveedor ha sido eliminado");
                limpiarFormulario();
            } catch (DAOException ex) {
                imprimirMensajeDeErrorDAO(ex);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int idProveedor = Integer.parseInt(txtBuscarPorIdProveedor.getText().trim());
            Proveedor miProveedor = manager.getProveedorDAO().obtener(idProveedor);

            if (miProveedor != null) {
                txtProveedor.setText(String.valueOf(miProveedor.getIdProveedor()));
                txtNombre.setText(miProveedor.getNombrePila());
                txtApellido.setText(miProveedor.getApellidoPaterno());
                txtTelefono.setText(miProveedor.getTelefono());
                txtEmail.setText(miProveedor.getEmail());
                band = false; // Ya no estamos insertando, sino editando
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró ninguna id");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Especifica un ID numérico válido", "Error", JOptionPane.ERROR_MESSAGE);
            txtBuscarPorIdProveedor.requestFocusInWindow();
            txtBuscarPorIdProveedor.selectAll();
        } catch (DAOException ex) {
            imprimirMensajeDeErrorDAO(ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

